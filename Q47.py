# Given two sets, demonstrate union, intersection, and difference.

# Example sets
set1 = {1, 2, 3, 4}
set2 = {3, 4, 5, 6}

# Perform set operations
union_result = set1 | set2  # Union
intersection_result = set1 & set2  # Intersection
difference_result = set1 - set2  # Difference

# Print the results
print("Union:", union_result)
print("Intersection:", intersection_result)
print("Difference (set1 - set2):", difference_result)
